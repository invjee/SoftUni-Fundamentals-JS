package AssociativeArraysEx;

public class Demo {
    public static void main(String[] args) {
        int[] newIntArray = new int[2];
        newIntArray[0]= 32;
        newIntArray[1]= 45;
        for (int i = 0; i < newIntArray.length; i++) {
            System.out.println(newIntArray[i]);
        }
    }
}
